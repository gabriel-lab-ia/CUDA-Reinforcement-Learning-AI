from __future__ import annotations

import os
from pathlib import Path

os.environ.setdefault("MPLCONFIGDIR", "/tmp/matplotlib")

import matplotlib.pyplot as plt

from cuda_rl.benchmarks.schemas import (
    BenchmarkAggregate,
    BenchmarkMetadata,
    BenchmarkRunResult,
)


def write_comparison_report(
    path: Path,
    *,
    metadata: BenchmarkMetadata,
    runs: list[BenchmarkRunResult],
    aggregates: list[BenchmarkAggregate],
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        f"# Benchmark Report: {metadata.suite}",
        "",
        "## Objective",
        "Record reproducible benchmark metadata and measured smoke results.",
        "",
        "## Methodology",
        "This report is generated by the command-line benchmark runner. "
        "Smoke results validate pipeline behavior and must not be interpreted "
        "as scientific performance conclusions.",
        "",
        "## Hardware and Software",
        f"- Commit: `{metadata.commit_sha}`",
        f"- Working tree: `{metadata.working_tree_status}`",
        f"- Python: `{metadata.python_version}`",
        f"- PyTorch: `{metadata.pytorch_version}`",
        f"- CUDA build: `{metadata.cuda_version}`",
        f"- Platform: `{metadata.platform}`",
        f"- CPU: `{metadata.cpu}`",
        f"- GPU: `{metadata.gpu or 'Not available'}`",
        "",
        "## Aggregate Results",
        "| Backend | Status | Seeds | Success rate | Mean steps/s | Mean wall time |",
        "|---|---|---:|---:|---:|---:|",
    ]
    for aggregate in aggregates:
        lines.append(
            "| "
            f"{aggregate.backend} | "
            f"{aggregate.status} | "
            f"{aggregate.seed_count} | "
            f"{aggregate.success_rate:.2f} | "
            f"{_fmt(aggregate.mean_steps_per_second)} | "
            f"{_fmt(aggregate.mean_wall_clock_seconds)} |"
        )
    lines.extend(
        [
            "",
            "## Limitations",
            "- Smoke benchmark defaults are intentionally short.",
            "- CUDA entries are marked not available when CUDA is unavailable.",
            "- No speedup claim is made without a completed comparison report.",
            "",
            "## Run Status",
            "| Backend | Seed | Status | Error |",
            "|---|---:|---|---|",
        ]
    )
    for run in runs:
        lines.append(
            f"| {run.backend} | {run.seed} | {run.status} | {run.error or ''} |"
        )
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_figures(
    figures_directory: Path,
    *,
    aggregates: list[BenchmarkAggregate],
) -> None:
    figures_directory.mkdir(parents=True, exist_ok=True)
    labels = [str(aggregate.backend) for aggregate in aggregates]
    throughputs = [aggregate.mean_steps_per_second or 0.0 for aggregate in aggregates]
    fig, axis = plt.subplots(figsize=(8, 4.5))
    axis.bar(labels, throughputs, color="#4C78A8")
    axis.set_title("Mean benchmark throughput")
    axis.set_xlabel("Backend")
    axis.set_ylabel("Steps per second")
    axis.grid(axis="y", alpha=0.25)
    fig.tight_layout()
    fig.savefig(figures_directory / "steps_per_second.png", dpi=160)
    plt.close(fig)


def _fmt(value: float | None) -> str:
    return "Not executed" if value is None else f"{value:.4f}"
